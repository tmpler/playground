var args = process.argv;

var message = args.slice(2);

var morse = "";

for(var i = 0; i<message.length;i++){
    var word = message[i];
    for(var j = 0; j<word.length;j++){
        if(MORSECODE[word[j].toUpperCase()]){
            morse += MORSECODE[word[j].toUpperCase()] + " ";
        } else {
            morse += word[j] + " " ;
        }
    }
    morse += " ";
}

console.log(morse);